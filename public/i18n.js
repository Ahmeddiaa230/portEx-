let currentLang = localStorage.getItem('portex_lang') || 'en';

function t(key) {
  return (i18n[currentLang] && i18n[currentLang][key]) || i18n.en[key] || key;
}

function applyTranslations(lang) {
  currentLang = lang;
  localStorage.setItem('portex_lang', lang);
  document.documentElement.dir = lang === 'ar' ? 'rtl' : 'ltr';
  document.documentElement.lang = lang;
  
  const switcher = document.getElementById('langSwitcher');
  if (switcher) switcher.value = lang;

  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    const val = t(key);
    if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
      if (el.placeholder) el.placeholder = val;
      if (el.type === 'submit' || el.type === 'button') el.value = val;
    } else {
      el.innerHTML = val;
    }
  });

  document.querySelectorAll('[data-tip]').forEach(el => {
    el.title = t(el.getAttribute('data-tip'));
  });

  // Call specific update auth UI if it exists globally
  if (typeof updateAuthUI === 'function') {
    updateAuthUI();
  }
}

// Listen for switcher change globally if present
document.addEventListener('DOMContentLoaded', () => {
  const switcher = document.getElementById('langSwitcher');
  if (switcher) {
    switcher.addEventListener('change', (e) => {
      applyTranslations(e.target.value);
    });
  }
  applyTranslations(currentLang);
});
